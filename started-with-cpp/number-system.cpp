#include <iostream>

int main() {
    int dec = 15;   // decimal whole number
    int binary = 0b01111; // binary
    int oct = 017; //Octal
    int hexDec = 0x0f; // Hexadecimal
    std::cout << "Decimal of " << dec << "\n";
    std::cout << "binary of " << binary << "\n";
    std::cout << "Octal of " << oct << "\n";
    std::cout << "Hexadecimal of " << hexDec << "\n";

    return 0;
}